def validate_column_code_based(
    col_name: str,
    df: pd.DataFrame,
    orig_df: Optional[pd.DataFrame],
    checks_config: dict,
    col_type: str,
) -> dict:
    """
    Run all code-based checks for a column based on checks_config.

    Supported check keys in checks_config:
        "null_rate": {"max": float}
        "value_range": {"min": float, "max": float}
        "distribution_match": {"test": "ks"|"chi_square"|"ks_length"|"auto", "min_pvalue": float}
        "vocabulary_overlap": {"min_jaccard": float}
        "schema_conformance": {schema dict}
        "allowed_values": [list]
        "correlation": {"column": str, "min_r": float, "method": str}

    Returns:
        dict with per-check results, pass, score, flagged_rows, suggestions
    """
    series = df[col_name] if col_name in df.columns else pd.Series([], dtype=object)
    orig_series = orig_df[col_name] if (orig_df is not None and col_name in orig_df.columns) else None

    check_results = {}
    flagged_indices = set()
    failed_checks = []

    # Null rate
    if "null_rate" in checks_config:
        cfg = checks_config["null_rate"]
        max_rate = cfg.get("max", DEFAULT_MAX_NULL_RATE)
        r = run_null_rate_check(series, max_rate=max_rate)
        check_results["null_rate"] = r
        if not r["pass"]:
            failed_checks.append("null_rate")

    # Value range
    if "value_range" in checks_config:
        cfg = checks_config["value_range"]
        r = run_value_range_check(series, min_val=cfg.get("min"), max_val=cfg.get("max"))
        check_results["value_range"] = r
        if not r["pass"]:
            failed_checks.append("value_range")
            flagged_indices.update(r.get("flagged_indices", []))

    # Distribution match
    if "distribution_match" in checks_config:
        cfg = checks_config["distribution_match"]
        r = run_distribution_match_check(
            synth_series=series,
            orig_series=orig_series,
            test=cfg.get("test", "auto"),
            min_pvalue=cfg.get("min_pvalue", DEFAULT_MIN_KS_PVALUE),
            col_type=col_type,
        )
        check_results["distribution_match"] = r
        if r.get("pass") is False:
            failed_checks.append("distribution_match")

    # Vocabulary overlap
    if "vocabulary_overlap" in checks_config:
        cfg = checks_config["vocabulary_overlap"]
        r = run_vocabulary_overlap_check(
            synth_series=series,
            orig_series=orig_series,
            min_jaccard=cfg.get("min_jaccard", 0.3),
        )
        check_results["vocabulary_overlap"] = r
        if r.get("pass") is False:
            failed_checks.append("vocabulary_overlap")

    # Schema conformance
    if "schema_conformance" in checks_config:
        r = run_schema_conformance_check(series, schema=checks_config["schema_conformance"])
        check_results["schema_conformance"] = r
        if not r["pass"]:
            failed_checks.append("schema_conformance")
            flagged_indices.update(r.get("flagged_indices", []))

    # Allowed values shorthand
    if "allowed_values" in checks_config:
        r = run_allowed_values_check(series, allowed=checks_config["allowed_values"])
        check_results["allowed_values"] = r
        if not r["pass"]:
            failed_checks.append("allowed_values")
            flagged_indices.update(r.get("flagged_indices", []))

    # Correlation
    if "correlation" in checks_config:
        r = run_correlation_check(df, col_name, checks_config["correlation"])
        check_results["correlation"] = r
        if r.get("pass") is False:
            failed_checks.append("correlation")

    # Compute score: fraction of checks that passed
    total_checks = len(check_results)
    # Count definitive pass/fail only (skip None-pass checks)
    definitive = [(k, v) for k, v in check_results.items() if v.get("pass") is not None]
    passed_count = sum(1 for _, v in definitive if v.get("pass") is True)
    score = round(passed_count / len(definitive), 4) if definitive else 1.0

    column_pass = len(failed_checks) == 0

    # Build flagged rows list
    flagged_rows = []
    for idx in sorted(flagged_indices)[:100]:
        try:
            val = df.at[idx, col_name]
        except (KeyError, IndexError):
            val = None
        flagged_rows.append({
            "row_index": int(idx),
            "value": str(val) if val is not None else None,
            "failed_checks": failed_checks,
        })

    # Generate suggestions
    suggestions = _generate_code_based_suggestions(failed_checks, check_results, col_type)

    return {
        "approach": "code_based",
        "col_type": col_type,
        "checks": check_results,
        "failed_checks": failed_checks,
        "pass": column_pass,
        "score": score,
        "flagged_rows": flagged_rows,
        "suggestions": suggestions,
    }


def _generate_code_based_suggestions(
    failed_checks: list[str],
    check_results: dict,
    col_type: str,
) -> list[str]:
    """Generate actionable suggestions based on failed code-based checks."""
    suggestions = []

    if "null_rate" in failed_checks:
        r = check_results["null_rate"]
        suggestions.append(
            f"High null rate ({r['observed_rate']:.1%}): add 'non-null output required' to agent "
            f"instruction; set target_rows=fill-missing in batch_synthesize to re-fill null rows."
        )

    if "value_range" in failed_checks:
        for v in check_results["value_range"].get("violations", []):
            if v["type"] == "below_min":
                suggestions.append(
                    f"Values below minimum {v['limit']} (min observed: {v['min_observed']}): "
                    f"add explicit range constraint to synthesis prompt. Example: "
                    f"'Value must be >= {v['limit']}.'"
                )
            elif v["type"] == "above_max":
                suggestions.append(
                    f"Values above maximum {v['limit']} (max observed: {v['max_observed']}): "
                    f"add explicit range constraint to synthesis prompt. Example: "
                    f"'Value must be <= {v['limit']}.'"
                )

    if "distribution_match" in failed_checks:
        r = check_results["distribution_match"]
        suggestions.append(
            f"Distribution mismatch (p-value={r.get('p_value', 'N/A')}): increase few-shot "
            f"examples from original data, or use statistical_sample strategy for this column."
        )

    if "vocabulary_overlap" in failed_checks:
        r = check_results["vocabulary_overlap"]
        suggestions.append(
            f"Low vocabulary overlap (jaccard={r.get('jaccard_similarity', 'N/A')}): include "
            f"domain-specific terminology in agent instructions; add reference data from original."
        )

    if "schema_conformance" in failed_checks:
        r = check_results["schema_conformance"]
        for rule in r.get("violation_rules", []):
            suggestions.append(
                f"Schema violation ({rule['rule']}): enforce in agent instruction. "
                f"Example: add output format constraints and validation step."
            )

    if "allowed_values" in failed_checks:
        r = check_results["allowed_values"]
        suggestions.append(
            f"Out-of-set values ({r['violation_count']} violations): add allowed values list "
            f"explicitly to the synthesis prompt."
        )

    if "correlation" in failed_checks:
        r = check_results["correlation"]
        suggestions.append(
            f"Low correlation with '{r.get('correlated_with')}' "
            f"(r={r.get('observed_r', 'N/A')}, required >= {r.get('min_r')}): include the "
            f"correlated column as a context dependency in synthesis config."
        )

    return suggestions


