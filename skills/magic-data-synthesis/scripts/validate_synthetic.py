def validate_column_combined(
    col_name: str,
    df: pd.DataFrame,
    orig_df: Optional[pd.DataFrame],
    checks_config: dict,
    rubric: str,
    sample_rate: float,
    pass_threshold: float,
    col_type: str,
    agent_yaml: Optional[str],
    model: Optional[str],
    cost_accumulator: CostAccumulator,
    sample_size: Optional[int] = None,
) -> dict:
    """
    Run code-based checks first, then LLM-as-judge on rows that pass code checks.

    Returns merged result dict.
    """
    # Step 1: Code-based checks
    code_result = validate_column_code_based(
        col_name=col_name,
        df=df,
        orig_df=orig_df,
        checks_config=checks_config,
        col_type=col_type,
    )

    # Filter to rows that passed code checks (not flagged)
    flagged_code_indices = set(r["row_index"] for r in code_result.get("flagged_rows", []))
    passing_df = df[~df.index.isin(flagged_code_indices)]

    # Step 2: LLM-as-judge on passing rows
    judge_result = validate_column_llm_judge(
        col_name=col_name,
        df=passing_df,
        rubric=rubric,
        sample_rate=sample_rate,
        pass_threshold=pass_threshold,
        agent_yaml=agent_yaml,
        model=model,
        cost_accumulator=cost_accumulator,
        sample_size=sample_size,
    )

    # Merge results
    combined_pass = code_result["pass"] and judge_result.get("pass", True)
    combined_score = round(
        (code_result["score"] + (judge_result.get("score") or 1.0)) / 2, 4
    )

    flagged_rows = code_result.get("flagged_rows", []) + judge_result.get("flagged_rows", [])
    suggestions = code_result.get("suggestions", []) + judge_result.get("suggestions", [])

    return {
        "approach": "combined",
        "col_type": col_type,
        "code_based": code_result,
        "llm_judge": judge_result,
        "pass": combined_pass,
        "score": combined_score,
        "flagged_rows": flagged_rows[:100],
        "suggestions": suggestions,
    }


