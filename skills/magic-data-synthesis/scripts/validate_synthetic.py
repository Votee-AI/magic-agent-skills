# Main Validation Orchestrator
# ──────────────────────────────────────────────────────────────

def validate_dataset(
    df: pd.DataFrame,
    orig_df: Optional[pd.DataFrame],
    columns: list[str],
    criteria: Optional[dict],
    sentinel_patterns: list[str],
    agent_yaml: Optional[str],
    model: Optional[str],
    cost_accumulator: CostAccumulator,
    llm_sample_size: Optional[int] = None,
) -> dict:
    """
    Orchestrate full dataset validation.

    For each column:
    1. Determine approach (from criteria or auto-detect)
    2. Run code-based, llm_judge, or combined checks
    3. Collect per-column results

    Returns:
        Full report dict (pre-summary)
    """
    col_types = detect_column_types(df)
    column_reports = {}

    # Global sentinel check
    sentinel_report = run_sentinel_check(df, columns, sentinel_patterns)

    for col_name in columns:
        logger.info("Validating column: %s", col_name)

        if col_name not in df.columns:
            column_reports[col_name] = {
                "approach": "skipped",
                "pass": None,
                "score": None,
                "note": f"Column '{col_name}' not found in dataset",
                "flagged_rows": [],
                "suggestions": [],
            }
            continue

        col_type = col_types.get(col_name, "unknown")

        # Resolve approach and config from criteria
        col_criteria = {}
        if criteria and "columns" in criteria:
            col_criteria = criteria["columns"].get(col_name, {})

        approach = col_criteria.get("approach", "auto")
        if approach == "auto":
            approach = detect_approach(col_name, df, col_types)

        checks_config = col_criteria.get("checks", {})
        rubric = col_criteria.get("rubric", "")
        sample_rate = col_criteria.get("sample_rate", DEFAULT_SAMPLE_RATE)
        pass_threshold = col_criteria.get("pass_threshold", DEFAULT_PASS_THRESHOLD)

        try:
            if approach == "code_based":
                result = validate_column_code_based(
                    col_name=col_name,
                    df=df,
                    orig_df=orig_df,
                    checks_config=checks_config,
                    col_type=col_type,
                )

            elif approach == "llm_judge":
                if not rubric:
                    rubric = (
                        f"Rate the quality of the synthesized '{col_name}' value on a scale "
                        f"of 0 to 1: relevance (0-1), accuracy (0-1), completeness (0-1). "
                        f"Pass if overall quality >= 0.6."
                    )
                result = validate_column_llm_judge(
                    col_name=col_name,
                    df=df,
                    rubric=rubric,
                    sample_rate=sample_rate,
                    pass_threshold=pass_threshold,
                    agent_yaml=agent_yaml,
                    model=model,
                    cost_accumulator=cost_accumulator,
                    sample_size=llm_sample_size,
                )

            elif approach == "combined":
                if not rubric:
                    rubric = (
                        f"Rate the quality of the synthesized '{col_name}' value: "
                        f"accuracy, completeness, relevance (0-1 each). Pass if all >= 0.6."
                    )
                result = validate_column_combined(
                    col_name=col_name,
                    df=df,
                    orig_df=orig_df,
                    checks_config=checks_config,
                    rubric=rubric,
                    sample_rate=sample_rate,
                    pass_threshold=pass_threshold,
                    col_type=col_type,
                    agent_yaml=agent_yaml,
                    model=model,
                    cost_accumulator=cost_accumulator,
                    sample_size=llm_sample_size,
                )

            else:
                result = {
                    "approach": approach,
                    "pass": None,
                    "score": None,
                    "note": f"Unknown approach '{approach}'; skipped",
                    "flagged_rows": [],
                    "suggestions": [f"Use approach: code_based, llm_judge, combined, or auto"],
                }

        except CostLimitExceeded:
            raise
        except Exception as e:
            logger.error("Validation failed for column '%s': %s", col_name, e)
            result = {
                "approach": approach,
                "pass": None,
                "score": None,
                "error": str(e),
                "flagged_rows": [],
                "suggestions": [f"Validation error on column '{col_name}': {e}"],
            }

        column_reports[col_name] = result

    return column_reports, sentinel_report


# ──────────────────────────────────────────────────────────────
# Report Assembly
# ──────────────────────────────────────────────────────────────

def assemble_report(
    column_reports: dict,
    sentinel_report: dict,
    df: pd.DataFrame,
    orig_df: Optional[pd.DataFrame],
    columns: list[str],
    cost_accumulator: CostAccumulator,
    elapsed_seconds: float,
    sample_size: Optional[int],
    metadata: dict,
) -> dict:
    """
    Assemble the final structured validation report.

    Returns:
        Full report dict matching the specified structure
    """
    # Determine per-column pass/fail
    col_verdicts = []
    for col_name in columns:
        col_result = column_reports.get(col_name, {})
        col_pass = col_result.get("pass")
        if col_pass is True:
            col_verdicts.append(1.0)
        elif col_pass is False:
            col_verdicts.append(0.0)
        # None = skip (unknown/error)

    # Compute column-level scores
    col_scores = []
    for col_name in columns:
        col_result = column_reports.get(col_name, {})
        score = col_result.get("score")
        if score is not None:
            col_scores.append(score)

    # Overall verdict
    definitive_verdicts = [v for col_name in columns
                           for v in [column_reports.get(col_name, {}).get("pass")]
                           if v is not None]

    sentinel_pass = sentinel_report.get("pass", True)
    all_pass = all(v is True for v in definitive_verdicts) and sentinel_pass

    overall_score = round(sum(col_scores) / len(col_scores), 4) if col_scores else 1.0
    overall_verdict = "PASS" if all_pass else "FAIL"

    # Summary counts
    passed_cols = sum(1 for col in columns if column_reports.get(col, {}).get("pass") is True)
    failed_cols = sum(1 for col in columns if column_reports.get(col, {}).get("pass") is False)
    error_cols = sum(1 for col in columns if column_reports.get(col, {}).get("pass") is None)

    summary = {
        "total_columns": len(columns),
        "columns_pass": passed_cols,
        "columns_fail": failed_cols,
        "columns_error_or_skipped": error_cols,
        "sentinel_check_pass": sentinel_pass,
        "total_rows": len(df),
        "original_rows": len(orig_df) if orig_df is not None else None,
        "sample_size": sample_size,
        "elapsed_seconds": round(elapsed_seconds, 2),
        "cost": cost_accumulator.summary(),
        "validated_at": datetime.now(timezone.utc).isoformat(),
    }

    report = {
        "success": True,
        "overall_verdict": overall_verdict,
        "overall_score": overall_score,
        "columns": column_reports,
        "sentinel_check": sentinel_report,
        "summary": summary,
    }

    # Merge any caller-provided metadata
    if metadata:
        report["metadata"] = metadata

    return report


