def build_judge_prompt(
    col_name: str,
    rubric: str,
    row: dict,
    value: Any,
) -> str:
    """
    Build a prompt for LLM-as-judge evaluation.

    Returns:
        Prompt string requesting JSON output: {"pass": bool, "score": float, "reason": str}
    """
    safe_row = sanitize_row_for_prompt(row, max_length=500)
    safe_value = str(value)[:1000] if value is not None else "(null)"

    # Remove target col from context to avoid circular reference
    context = {k: v for k, v in safe_row.items() if k != col_name}
    context_str = "\n".join(f"  {k}: {v}" for k, v in list(context.items())[:10])

    return f"""You are a data quality judge. Evaluate the synthesized value for column '{col_name}'.

Row context:
{context_str}

Synthesized value for '{col_name}':
{safe_value}

Evaluation rubric:
{rubric}

Respond ONLY with a JSON object (no markdown, no explanation):
{{"pass": true or false, "score": float between 0 and 1, "reason": "brief reason"}}"""


def validate_column_llm_judge(
    col_name: str,
    df: pd.DataFrame,
    rubric: str,
    sample_rate: float,
    pass_threshold: float,
    agent_yaml: Optional[str],
    model: Optional[str],
    cost_accumulator: CostAccumulator,
    sample_size: Optional[int] = None,
) -> dict:
    """
    Run LLM-as-judge evaluation on a sampled subset of rows.

    Args:
        col_name: Column to evaluate
        df: Synthetic DataFrame
        rubric: Rubric string for the judge
        sample_rate: Fraction of rows to sample (0–1)
        pass_threshold: Fraction of judged rows that must pass (0–1)
        agent_yaml: Path to agent YAML (or None to use inline config)
        model: Model override
        cost_accumulator: Cost tracker
        sample_size: Override sample count (ignores sample_rate if set)

    Returns:
        dict with judge results, pass, score, flagged_rows, suggestions
    """
    if col_name not in df.columns:
        return {
            "approach": "llm_judge",
            "pass": None,
            "score": None,
            "note": f"Column '{col_name}' not found in dataset",
            "flagged_rows": [],
            "suggestions": [],
        }

    series = df[col_name]
    non_null_idx = series.dropna().index.tolist()
    total_rows = len(non_null_idx)

    if total_rows == 0:
        return {
            "approach": "llm_judge",
            "pass": False,
            "score": 0.0,
            "note": "All values are null; cannot judge",
            "flagged_rows": [],
            "suggestions": ["All values are null. Re-run synthesis with fill-missing mode."],
        }

    # Determine sample count
    if sample_size is not None:
        n_sample = min(sample_size, total_rows)
    else:
        n_sample = max(1, min(total_rows, int(math.ceil(total_rows * sample_rate))))

    # Random sample (reproducible)
    rng = np.random.default_rng(42)
    sampled_indices = rng.choice(non_null_idx, size=n_sample, replace=False).tolist()

    # Build or load judge agent
    agent = _build_judge_agent(agent_yaml=agent_yaml, model=model)

    # Run judge on each sampled row
    judged_results = []
    flagged_rows = []

    for idx in sampled_indices:
        if cost_accumulator.max_cost is not None and not cost_accumulator.check_budget(0.001):
            logger.warning("Cost limit approaching; stopping LLM judge sampling early")
            break

        row = df.loc[idx].to_dict()
        value = row.get(col_name)

        prompt = build_judge_prompt(col_name=col_name, rubric=rubric, row=row, value=value)

        try:
            session_id = str(uuid.uuid4())
            response_text = run_agent(agent, prompt, session_id=session_id)

            # Parse JSON response
            judge_result = _parse_judge_response(response_text)

            # Track cost estimate
            estimated_cost = cost_accumulator.estimate_cost(
                prompt_tokens=len(prompt) // 4,
                completion_tokens=60,
                model=model or DEFAULT_MODEL,
            )
            cost_accumulator.total_usd += estimated_cost
            cost_accumulator.call_count += 1
            m_key = model or DEFAULT_MODEL
            cost_accumulator.costs_by_model[m_key] = (
                cost_accumulator.costs_by_model.get(m_key, 0.0) + estimated_cost
            )

            judged_results.append({
                "row_index": int(idx),
                "pass": judge_result.get("pass", False),
                "score": judge_result.get("score", 0.0),
                "reason": judge_result.get("reason", ""),
            })

            if not judge_result.get("pass", True):
                flagged_rows.append({
                    "row_index": int(idx),
                    "value": str(value)[:200] if value is not None else None,
                    "judge_score": judge_result.get("score"),
                    "judge_reason": judge_result.get("reason", ""),
                    "column": col_name,
                    "criterion": "llm_judge",
                })

        except CostLimitExceeded:
            raise
        except Exception as e:
            logger.warning("LLM judge failed for row %d, column '%s': %s", idx, col_name, e)
            judged_results.append({
                "row_index": int(idx),
                "pass": None,
                "score": None,
                "reason": f"Judge error: {e}",
                "error": True,
            })

    # Aggregate results
    definitive = [r for r in judged_results if r.get("pass") is not None]
    passed_count = sum(1 for r in definitive if r["pass"] is True)
    n_judged = len(definitive)

    pass_rate = round(passed_count / n_judged, 6) if n_judged > 0 else 0.0
    avg_score = round(
        sum(r["score"] for r in definitive if r.get("score") is not None)
        / max(1, sum(1 for r in definitive if r.get("score") is not None)),
        4,
    )

    column_pass = pass_rate >= pass_threshold

    # 95% confidence interval (normal approximation)
    confidence_interval = None
    if n_judged >= 10:
        margin = 1.96 * math.sqrt(pass_rate * (1 - pass_rate) / n_judged)
        confidence_interval = {
            "lower": round(max(0.0, pass_rate - margin), 4),
            "upper": round(min(1.0, pass_rate + margin), 4),
        }

    suggestions = _generate_judge_suggestions(pass_rate, pass_threshold, col_name, flagged_rows)

    return {
        "approach": "llm_judge",
        "rubric": rubric,
        "sample_size": n_sample,
        "total_rows": total_rows,
        "sample_rate": round(n_sample / total_rows, 4) if total_rows > 0 else 0.0,
        "n_judged": n_judged,
        "pass_rate": pass_rate,
        "pass_threshold": pass_threshold,
        "avg_score": avg_score,
        "pass": column_pass,
        "score": pass_rate,
        "confidence_interval": confidence_interval,
        "judged_sample": judged_results,
        "flagged_rows": flagged_rows[:50],  # cap for report size
        "suggestions": suggestions,
    }


def _build_judge_agent(agent_yaml: Optional[str], model: Optional[str]):
    """Build or create a judge agent from YAML or inline config."""
    if agent_yaml and Path(agent_yaml).exists():
        return build_agent(agent_yaml, model_override=model)

    # Create inline config
    import tempfile
    import yaml

    resolved_model = model or DEFAULT_MODEL
    config = {
        "name": "validate_judge",
        "model": resolved_model,
        "instruction": (
            "You are a data quality judge. Evaluate synthesized data values against the given "
            "rubric. Always respond with a valid JSON object: "
            '{\"pass\": bool, \"score\": 0.0-1.0, \"reason\": \"brief explanation\"}'
        ),
    }

    # Write to a temp file
    tmp = tempfile.NamedTemporaryFile(
        mode="w", suffix=".yaml", delete=False, prefix="judge_agent_"
    )
    yaml.safe_dump(config, tmp)
    tmp.flush()
    tmp.close()

    return build_agent(tmp.name, model_override=resolved_model)


def _parse_judge_response(response_text: str) -> dict:
    """
    Parse judge LLM response into a structured dict.

    Handles JSON embedded in text or markdown code fences.
    Falls back to heuristic parsing on failure.
    """
    text = response_text.strip()

    # Strip markdown code fences
    if "```" in text:
        text = re.sub(r"```(?:json)?\n?", "", text).strip()
        text = text.rstrip("`").strip()

    # Try direct JSON parse
    try:
        result = json.loads(text)
        if isinstance(result, dict):
            return result
    except json.JSONDecodeError:
        pass

    # Try to extract JSON substring
    match = re.search(r"\{[^}]+\}", text, re.DOTALL)
    if match:
        try:
            result = json.loads(match.group())
            if isinstance(result, dict):
                return result
        except json.JSONDecodeError:
            pass

    # Heuristic fallback
    lower = text.lower()
    passed = "pass" in lower and "fail" not in lower and "false" not in lower
    score = 0.8 if passed else 0.2

    return {
        "pass": passed,
        "score": score,
        "reason": text[:200],
        "parse_fallback": True,
    }


def _generate_judge_suggestions(
    pass_rate: float,
    pass_threshold: float,
    col_name: str,
    flagged_rows: list[dict],
) -> list[str]:
    """Generate actionable suggestions based on LLM judge results."""
    suggestions = []

    if pass_rate < pass_threshold:
        gap = pass_threshold - pass_rate
        suggestions.append(
            f"LLM judge pass rate ({pass_rate:.1%}) is below threshold ({pass_threshold:.1%}). "
            f"Gap: {gap:.1%}. Recommended actions:"
        )
        suggestions.append(
            "  - Add more domain-specific few-shot examples to the synthesis prompt."
        )
        suggestions.append(
            "  - Lower temperature or increase max_tokens for the synthesis model."
        )
        suggestions.append(
            f"  - Add explicit quality instructions to agent YAML for column '{col_name}'."
        )
        if flagged_rows:
            sample_reasons = list(set(
                r.get("judge_reason", "") for r in flagged_rows[:5] if r.get("judge_reason")
            ))
            if sample_reasons:
                suggestions.append(
                    f"  - Common failure reasons: {'; '.join(sample_reasons[:3])}"
                )

    return suggestions


