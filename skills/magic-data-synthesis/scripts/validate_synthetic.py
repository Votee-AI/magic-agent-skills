def run_null_rate_check(series: pd.Series, max_rate: float) -> dict:
    """
    Check that the null rate is within the configured limit.

    Returns:
        check result dict with pass/fail, observed_rate, threshold
    """
    total = len(series)
    null_count = int(series.isna().sum())
    observed_rate = round(null_count / total, 6) if total > 0 else 0.0
    passed = observed_rate <= max_rate
    return {
        "check": "null_rate",
        "pass": passed,
        "observed_rate": observed_rate,
        "threshold": max_rate,
        "null_count": null_count,
        "total": total,
    }


def run_distribution_match_check(
    synth_series: pd.Series,
    orig_series: Optional[pd.Series],
    test: str,
    min_pvalue: float,
    col_type: str,
) -> dict:
    """
    Run a statistical distribution match check.

    For numeric columns: KS test (or as configured).
    For categorical columns: chi-square test.
    For text columns: KS test on character length distribution.

    Returns:
        check result dict
    """
    if orig_series is None:
        return {
            "check": "distribution_match",
            "pass": None,
            "note": "No original data provided; distribution check skipped",
        }

    # Determine effective test type
    effective_test = test
    if effective_test == "auto":
        if col_type in ("numeric",):
            effective_test = "ks"
        elif col_type == "categorical":
            effective_test = "chi_square"
        else:
            # Text: use KS on lengths
            effective_test = "ks_length"

    if effective_test == "ks":
        result = ks_test(orig_series, synth_series)
        result["check"] = "distribution_match"
        result["test_used"] = "ks"
        result["min_pvalue"] = min_pvalue
        if result.get("p_value") is not None:
            result["pass"] = result["p_value"] >= min_pvalue
        return result

    if effective_test == "chi_square":
        result = chi_square_test(orig_series, synth_series)
        result["check"] = "distribution_match"
        result["test_used"] = "chi_square"
        result["min_pvalue"] = min_pvalue
        if result.get("p_value") is not None:
            result["pass"] = result["p_value"] >= min_pvalue
        return result

    if effective_test == "ks_length":
        # Compare length distributions for text columns
        orig_lengths = orig_series.dropna().astype(str).str.len().rename("length")
        synth_lengths = synth_series.dropna().astype(str).str.len().rename("length")
        result = ks_test(orig_lengths, synth_lengths)
        result["check"] = "distribution_match"
        result["test_used"] = "ks_length"
        result["min_pvalue"] = min_pvalue
        if result.get("p_value") is not None:
            result["pass"] = result["p_value"] >= min_pvalue
        return result

    return {
        "check": "distribution_match",
        "pass": None,
        "note": f"Unknown test type: {test}",
    }


def run_value_range_check(series: pd.Series, min_val: Optional[float], max_val: Optional[float]) -> dict:
    """
    Check that all values fall within [min_val, max_val].

    Returns:
        check result dict with flagged row indices
    """
    result = check_value_range(series, min_val=min_val, max_val=max_val)
    result["check"] = "value_range"

    # Collect flagged row indices (up to 100)
    numeric = pd.to_numeric(series.dropna(), errors="coerce").dropna()
    flagged_indices = []
    if min_val is not None:
        below_mask = numeric < min_val
        flagged_indices.extend(numeric[below_mask].index.tolist())
    if max_val is not None:
        above_mask = numeric > max_val
        flagged_indices.extend(numeric[above_mask].index.tolist())

    result["flagged_indices"] = sorted(set(int(i) for i in flagged_indices))[:100]
    return result


def run_allowed_values_check(series: pd.Series, allowed: list) -> dict:
    """
    Check that all values are in the allowed set.

    Returns:
        check result dict with violation count and flagged indices
    """
    non_null = series.dropna()
    total = len(non_null)
    allowed_set = set(str(v) for v in allowed)
    str_vals = non_null.astype(str)
    violations = str_vals[~str_vals.isin(allowed_set)]
    violation_count = len(violations)
    passed = violation_count == 0

    return {
        "check": "allowed_values",
        "pass": passed,
        "total": total,
        "violation_count": violation_count,
        "violation_rate": round(violation_count / total, 6) if total > 0 else 0.0,
        "flagged_indices": sorted(int(i) for i in violations.index.tolist())[:100],
        "sample_violations": violations.head(10).tolist(),
    }


def run_schema_conformance_check(series: pd.Series, schema: dict) -> dict:
    """
    Check schema conformance: type, non-null, non-empty, min/max length, regex, allowed values.

    Schema keys (all optional):
        "non_null": bool
        "non_empty": bool (for strings)
        "dtype": str — expected dtype family ("numeric", "string", "integer")
        "min_length": int — minimum string length
        "max_length": int — maximum string length
        "regex": str — regex pattern that values must match
        "allowed_values": list

    Returns:
        check result dict
    """
    total = len(series)
    violations = []
    flagged_indices = set()

    # Non-null check
    if schema.get("non_null", False):
        null_mask = series.isna()
        null_count = int(null_mask.sum())
        if null_count > 0:
            violations.append({
                "rule": "non_null",
                "violation_count": null_count,
                "violation_rate": round(null_count / total, 6) if total > 0 else 0.0,
            })
            flagged_indices.update(series[null_mask].index.tolist())

    str_vals = series.dropna().astype(str)

    # Non-empty check
    if schema.get("non_empty", False):
        empty_mask = str_vals.str.strip() == ""
        empty_count = int(empty_mask.sum())
        if empty_count > 0:
            violations.append({
                "rule": "non_empty",
                "violation_count": empty_count,
                "violation_rate": round(empty_count / len(str_vals), 6) if len(str_vals) > 0 else 0.0,
            })
            flagged_indices.update(str_vals[empty_mask].index.tolist())

    # Min/max length
    if "min_length" in schema or "max_length" in schema:
        lengths = str_vals.str.len()
        if "min_length" in schema:
            short = str_vals[lengths < schema["min_length"]]
            if len(short) > 0:
                violations.append({
                    "rule": "min_length",
                    "threshold": schema["min_length"],
                    "violation_count": len(short),
                })
                flagged_indices.update(short.index.tolist())
        if "max_length" in schema:
            long_vals = str_vals[lengths > schema["max_length"]]
            if len(long_vals) > 0:
                violations.append({
                    "rule": "max_length",
                    "threshold": schema["max_length"],
                    "violation_count": len(long_vals),
                })
                flagged_indices.update(long_vals.index.tolist())

    # Regex check
    if "regex" in schema:
        pattern = re.compile(schema["regex"])
        mismatch = str_vals[~str_vals.str.match(pattern)]
        if len(mismatch) > 0:
            violations.append({
                "rule": "regex",
                "pattern": schema["regex"],
                "violation_count": len(mismatch),
                "sample_violations": mismatch.head(5).tolist(),
            })
            flagged_indices.update(mismatch.index.tolist())

    # Allowed values
    if "allowed_values" in schema:
        allowed_set = set(str(v) for v in schema["allowed_values"])
        bad = str_vals[~str_vals.isin(allowed_set)]
        if len(bad) > 0:
            violations.append({
                "rule": "allowed_values",
                "allowed": schema["allowed_values"][:20],
                "violation_count": len(bad),
                "sample_violations": bad.head(5).tolist(),
            })
            flagged_indices.update(bad.index.tolist())

    passed = len(violations) == 0
    return {
        "check": "schema_conformance",
        "pass": passed,
        "total": total,
        "violation_rules": violations,
        "flagged_indices": sorted(int(i) for i in flagged_indices)[:100],
    }


def run_correlation_check(
    df: pd.DataFrame,
    col_name: str,
    correlation_with: dict,
) -> dict:
    """
    Check correlation between a numeric column and another column in the DataFrame.

    correlation_with format: {"column": "other_col", "min_r": 0.7, "method": "pearson"}

    Returns:
        check result dict
    """
    other_col = correlation_with.get("column")
    min_r = correlation_with.get("min_r", 0.5)
    method = correlation_with.get("method", "pearson")

    if not other_col or other_col not in df.columns:
        return {
            "check": "correlation",
            "pass": None,
            "note": f"Correlation target column '{other_col}' not found in dataset",
        }

    col_num = pd.to_numeric(df[col_name].dropna(), errors="coerce").dropna()
    other_num = pd.to_numeric(df[other_col].dropna(), errors="coerce").dropna()

    # Align indices
    common_idx = col_num.index.intersection(other_num.index)
    if len(common_idx) < 2:
        return {
            "check": "correlation",
            "pass": None,
            "note": "Insufficient overlapping numeric values for correlation",
        }

    a = col_num.loc[common_idx]
    b = other_num.loc[common_idx]

    if method == "spearman":
        from scipy import stats
        r, p_value = stats.spearmanr(a, b)
    else:
        r = float(a.corr(b, method="pearson"))
        p_value = None

    passed = abs(r) >= min_r
    result = {
        "check": "correlation",
        "pass": passed,
        "column": col_name,
        "correlated_with": other_col,
        "method": method,
        "observed_r": round(r, 6),
        "min_r": min_r,
        "n": len(common_idx),
    }
    if p_value is not None:
        result["p_value"] = round(float(p_value), 6)
    return result


def run_vocabulary_overlap_check(
    synth_series: pd.Series,
    orig_series: Optional[pd.Series],
    min_jaccard: float = 0.3,
) -> dict:
    """
    Check vocabulary overlap between synthetic and original text columns.

    Returns:
        check result dict
    """
    if orig_series is None:
        return {
            "check": "vocabulary_overlap",
            "pass": None,
            "note": "No original data provided; vocabulary check skipped",
        }

    result = vocabulary_overlap(orig_series, synth_series)
    result["check"] = "vocabulary_overlap"
    result["min_jaccard"] = min_jaccard
    result["pass"] = result.get("jaccard_similarity", 0.0) >= min_jaccard
    return result


