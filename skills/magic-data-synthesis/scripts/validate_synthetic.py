def detect_approach(col_name: str, df: pd.DataFrame, col_types: dict) -> str:
    """
    Automatically determine the best validation approach for a column.

    Numeric/datetime/boolean -> code_based
    Text (long strings, high cardinality) -> llm_judge
    Categorical -> code_based
    Mixed/unknown -> code_based (conservative)

    Returns:
        "code_based" or "llm_judge"
    """
    col_type = col_types.get(col_name, "unknown")
    if col_type in ("numeric", "datetime", "boolean", "categorical"):
        return "code_based"
    if col_type == "text":
        return "llm_judge"
    # mixed / empty / unknown -> code-based (cheap, conservative)
    return "code_based"


