def run_sentinel_check(df: pd.DataFrame, columns: list[str], patterns: list[str]) -> dict:
    """
    Scan all requested columns for regenerated sentinel values.

    Returns:
        dict with per-column sentinel detection results and overall summary
    """
    column_results = {}
    total_sentinel_count = 0

    for col in columns:
        if col not in df.columns:
            continue
        result = detect_sentinels(df[col], patterns=patterns)
        column_results[col] = result
        total_sentinel_count += result.get("sentinel_count", 0)

    overall_pass = total_sentinel_count == 0
    return {
        "pass": overall_pass,
        "total_sentinel_count": total_sentinel_count,
        "patterns_checked": patterns,
        "columns": column_results,
    }


